import logging


def configure_logging(log_level):
    # Configure logging
    logger = logging.getLogger("remote_vector_index_builder")
    logger.basicConfig(
        level=log_level,
        format="%(asctime)s - %(name)s - %(levelname)s - %(message)s",
        datefmt="%Y-%m-%d %H:%M:%S",
    )
